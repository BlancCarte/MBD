package user;

public class USER {
	private String USER_ID;
	private String USER_PW;
	private String USER_NAME;
	private String USER_BIRTH;
	private String USER_GENDER;
	private String USER_EMAIL;
	private String USER_PHONE;

	public String getUSER_ID() {
		return USER_ID;
	}

	public void setUSER_ID(String SG_ID) {
		USER_ID = SG_ID;
	}

	public String getUSER_PW() {
		return USER_PW;
	}

	public void setUSER_PW(String SG_PW) {
		USER_PW = SG_PW;
	}

	public String getUSER_NAME() {
		return USER_NAME;
	}

	public void setUSER_NAME(String SG_NAME) {
		USER_NAME = SG_NAME;
	}

	public String getUSER_BIRTH() {
		return USER_BIRTH;
	}

	public void setUSER_BIRTH(String SG_BIRTH) {
		USER_BIRTH = SG_BIRTH;
	}

	public String getUSER_GENDER() {
		return USER_GENDER;
	}

	public void setUSER_GENDER(String SG_GENDER) {
		USER_GENDER = SG_GENDER;
	}

	public String getUSER_EMAIL() {
		return USER_EMAIL;
	}

	public void setUSER_EMAIL(String SG_EMAIL) {
		USER_EMAIL = SG_EMAIL;
	}

	public String getUSER_PHONE() {
		return USER_PHONE;
	}

	public void setUSER_PHONE(String SG_PHONE) {
		USER_PHONE = SG_PHONE;
	}

}
